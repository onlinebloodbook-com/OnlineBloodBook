	public void setHpName(String hpName) {
		this.hpName = hpName;
	}
	public String getHpContactNo() {
		return hpContactNo;
	}
	public void setHpContactNo(String hpContactNo) {
		this.hpContactNo = hpContactNo;
	}
	public String getHpEmail() {
		return hpEmail;
	}
